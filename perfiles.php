<script>

	// Hover
	$(".img-perfil").hover(function(){
		var valorCaja = $(this).attr('alt'); // Tomamos el Valor
		//show
		$(".ver-conten-"+valorCaja).css('display','block');
		$(".opacidad-"+valorCaja).css('display','block');
		$(".ver-conten-"+valorCaja).animate({
			marginTop:'50%'
		})
	},function(){
		var valorCaja = $(this).attr('alt'); // Tomamos el Valor
		//None
		$(".ver-conten-"+valorCaja).css('display','none');
		$(".opacidad-"+valorCaja).css('display','none');
		$(".ver-conten-"+valorCaja).animate({
			marginTop:'0%'
		})
	})



	function ver(valor){ // Efecto ver 

		$('.ver-conten-'+valor).css('display','none'); // Ocultamos el Boton

		$(".ver-conten-"+valor).removeClass('ver-conten-'+valor); // Elimina el Efecto Hover

		$(".fondo-blanco-"+valor).css('display','block') // Mostramos el Fondo Blanco

		// Proceso de animacion del fondo y el contenido
		$(".fondo-blanco-"+valor).animate({
			marginTop:'+0px',
			height:"+=340px",
		},1000,function(){
			$(".fondo-blanco-"+valor).css("margin-top",'0px');
			$(".info-"+valor).css("display",'block');
			$(".close-form-"+valor).css('display','block');
		});

	}

	function cerrar(valor){
		
		$('.close-form-'+valor).css('display','none');
		$("info-"+valor).css('display','none');
		
		$(".fondo-blanco-"+valor).animate({
			marginTop:'+=80%',
			height:"5px"
		}, 1000,function(){
			$(".fondo-blanco-"+valor).css('display','none');
		});	

		// Agregamos opacidad y la clase content
		$(".lugar-opacidad").addClass('opacidad');
		$("."+valor).addClass('ver-conten-'+valor);
		$(".ver-conten-"+valor).css('margin-top','50%');	

	}
</script>